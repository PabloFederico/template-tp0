package ar.fiuba.tdd.template.tp0;

import org.junit.Test;

import java.util.List;
import java.util.regex.Matcher;
import java.util.regex.Pattern;

import static org.junit.Assert.assertTrue;

public class RegExGeneratorTest {

    private boolean validate(String regEx, int numberOfResults) {

        int maxLength = 10;
        RegExGenerator generator = new RegExGenerator(maxLength);
        List<String> results;

            results = generator.generate(regEx, numberOfResults);

        // force matching the beginning and the end of the strings
        Pattern pattern = Pattern.compile("^" + regEx + "$", Pattern.DOTALL);
        return results
                .stream().reduce(true,
                        (acc, item) -> {
                            Matcher matcher = pattern.matcher(item);
                            return acc && matcher.find();
                        },
                        (item1, item2) -> item1 && item2);
    }


    @Test
    public void testAnyCharacter() {
        assertTrue(validate(".", 1));
    }

    @Test
    public void testMultipleCharacters() {
        assertTrue(validate("...", 1));
    }

    @Test
    public void testLiteral() {
        assertTrue(validate("\\@", 1));
    }

    @Test
    public void testLiteralDotCharacter() {
        assertTrue(validate("\\@..", 1));
    }

    @Test
    public void testZeroOrOneCharacter() {
        assertTrue(validate("\\@.h?", 1));
    }

    @Test
    public void testCharacterSet() {
        assertTrue(validate("[abc]", 1));
    }

    @Test
    public void testCharacterSetWithQuantifiers() {
        assertTrue(validate("[abc]+", 1));
    }

    //My Tests
    @Test
    public void test1() {
        assertTrue(validate("p.blo", 1));
    }

    @Test
    public void test2() {
        assertTrue(validate("p.*", 1));
    }

    @Test
    public void test3() {
        assertTrue(validate("a?b*c", 1));
    }

    @Test
    public void test4() {
        assertTrue(validate("..a+", 1));
    }

    @Test
    public void test5() {
        assertTrue(validate("td[d]", 1));
    }

    @Test
    public void test6() { assertTrue(validate("a+b?cde", 1)); }

    @Test
    public void test7() {
        assertTrue(validate("a[bc]d", 1));
    }

    @Test
    public void test8() {
        assertTrue(validate(".a*b?", 1));
    }



}